  getSuggestedQuestions(grade, count = 5) {
    const gradeQA = this[grade] || this.freshman;
    const generalPicks = this.general
      .sort(() => Math.random() - 0.5)
      .slice(0, 2);

    const gradePicks = [...gradeQA]
      .sort(() => Math.random() - 0.5)
      .slice(0, count - 2);

    return [...gradePicks, ...generalPicks]
      .sort(() => Math.random() - 0.5)
      .slice(0, count)
      .map(item => ({
        q: item.q,
        icon: this._getIconForQuestion(item.q)
      }));
  },

